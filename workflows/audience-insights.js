export const meta = {
  name: 'audience-insights',
  description: 'Анализ аудитории: разбирает кастдевы / комментарии / анкеты веером и собирает боли, возражения, желания, сегменты и банк дословных цитат.',
  phases: [
    { title: 'Разбор', detail: 'агент на каждый источник' },
    { title: 'Синтез', detail: 'боли, возражения, сегменты' },
  ],
}

// КАК ПЕРЕДАТЬ ДАННЫЕ:
//   args = { sources: ["текст кастдева 1", "пачка комментариев", "ответы анкеты"] }
//   или просто args = ["источник 1", "источник 2", ...]
const input = args || {}
let sources = Array.isArray(input.sources) ? input.sources : (Array.isArray(args) ? args : [])

if (!sources.length) {
  log('Передай тексты: args = { sources: ["кастдев...","комментарии...","анкета..."] }')
  return { error: 'no sources' }
}

const MAX = 60
if (sources.length > MAX) {
  log(`Источников много: беру первые ${MAX} из ${sources.length}.`)
  sources = sources.slice(0, MAX)
}

const EX = {
  type: 'object',
  properties: {
    pains: { type: 'array', items: { type: 'string' } },
    objections: { type: 'array', items: { type: 'string' } },
    desires: { type: 'array', items: { type: 'string' } },
    quotes: { type: 'array', items: { type: 'string' } },
  },
  required: ['pains'],
}

// ФАЗА 1 — разбор веером. Дешёвая модель haiku: извлечение по тексту.
phase('Разбор')
const ext = await parallel(sources.map((s, i) => () =>
  agent(
    `# Роль
Ты — UX/customer-research аналитик, который слышит «голос клиента» в сыром тексте.

# Контекст
Это голос аудитории, источник #${i + 1}:
${typeof s === 'string' ? s : JSON.stringify(s)}

# Задача
Вытащи из текста:
1. Боли — что мешает, что болит, чего боятся.
2. Возражения — почему сомневаются, не покупают, откладывают.
3. Желания — какого результата хотят, к чему стремятся.
4. Цитаты — дословные фразы, которые ярко передают боль/желание (язык аудитории).

# Ограничения
- Только то, что реально есть в тексте. Не домысливай и не обобщай за пределы источника.
- Цитаты — слово в слово, как написал человек (это важно для контента и продаж).

# Формат
Верни строго по JSON-схеме (pains, objections, desires, quotes).`,
    { label: `parse:${i + 1}`, phase: 'Разбор', model: 'haiku', schema: EX }
  )
)).then((r) => r.filter(Boolean))

// ФАЗА 2 — синтез. Умная модель opus только на финал.
phase('Синтез')
const report = await agent(
  `# Роль
Ты — research lead, который превращает разбор откликов в карту аудитории.

# Контекст
Разбор ${ext.length} источников, JSON:
${JSON.stringify(ext, null, 2)}

# Задача
Собери отчёт на русском:
1. Топ болей по частоте (с примерной частотностью).
2. Топ возражений.
3. Ключевые желания.
4. 2–3 сегмента аудитории — у каждого: кто это, главная боль, что им продавать и как говорить.
5. Банк дословных цитат, сгруппированный по болям/желаниям, — для контента и продаж.

# Ограничения
- Опирайся только на данные выше.
- Сегменты должны заметно отличаться друг от друга, а не быть тремя названиями одного и того же.

# Формат
Чистый Markdown.`,
  { label: 'synthesize', phase: 'Синтез', model: 'opus' }
)

return report
